package br.inatel.cdg.algebra.reta;

public class Ponto {

    private double x, y;

    //Construtor
    public Ponto(double x, double y){
        this.x = x;
        this.y = y;
    }

    //Getter para X
    public double getX() {
        return x;
    }

    //Getter paraY
    public double getY() {
        return y;
    }
}
